namespace RadiUX.Model {

	/*================================================================================================*/
	public class Vec3 : Vec2 {

		public float Z { get; set; }


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Vec3() {
		}

		/*--------------------------------------------------------------------------------------------*/
		public Vec3(float pX, float pY, float pZ) : base(pX, pY) {
			Z = pZ;
		}

	}

}
