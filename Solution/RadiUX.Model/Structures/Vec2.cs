namespace RadiUX.Model.Structures {

	/*================================================================================================*/
	public class Vec2 {

		public float X { get; set; }
		public float Y { get; set; }


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Vec2() {
		}

		/*--------------------------------------------------------------------------------------------*/
		public Vec2(float pX, float pY) {
			X = pX;
			Y = pY;
		}

	}

}
