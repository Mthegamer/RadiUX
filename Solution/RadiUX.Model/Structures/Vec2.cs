namespace RadiUX.Model.Structures {

	/*================================================================================================*/
	public struct Vec2 {

		public float X;
		public float Y;


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Vec2(float pX, float pY) {
			X = pX;
			Y = pY;
		}


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public static Vec2 operator +(Vec2 pA, Vec2 pB) {
			return new Vec2(pA.X+pB.X, pA.Y+pB.Y);
		}

		/*--------------------------------------------------------------------------------------------*/
		public static bool operator ==(Vec2 pA, Vec2 pB) {
			return (pA.X == pB.X && pA.Y == pB.Y);
		}
		
		/*--------------------------------------------------------------------------------------------*/
		public static bool operator !=(Vec2 pA, Vec2 pB) {
			return !(pA == pB);
		}

	}

}
