namespace RadiUX.Model.Structures {

	/*================================================================================================*/
	public struct Vec3 {
		
		public float X;
		public float Y;
		public float Z;


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public Vec3(float pX, float pY, float pZ) {
			X = pX;
			Y = pY;
			Z = pZ;
		}


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		public static Vec3 operator +(Vec3 pA, Vec3 pB) {
			return new Vec3(pA.X+pB.X, pA.Y+pB.Y, pA.Z+pB.Z);
		}

		/*--------------------------------------------------------------------------------------------*/
		public static bool operator ==(Vec3 pA, Vec3 pB) {
			return (pA.X == pB.X && pA.Y == pB.Y && pA.Z == pB.Z);
		}

		/*--------------------------------------------------------------------------------------------*/
		public static bool operator !=(Vec3 pA, Vec3 pB) {
			return !(pA == pB);
		}

	}

}
