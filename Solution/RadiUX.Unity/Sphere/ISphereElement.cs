using RadiUX.Model.Sphere;
using UnityEngine;

namespace RadiUX.Unity.Sphere {

	/*================================================================================================*/
	public interface ISphereElement {


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		Vector3 GetCenter();

		/*--------------------------------------------------------------------------------------------*/
		void SetCenter(Vector3 pCenter);
		
		/*--------------------------------------------------------------------------------------------*/
		Element GetElementData();


		////////////////////////////////////////////////////////////////////////////////////////////////
		/*--------------------------------------------------------------------------------------------*/
		void FindChildren();

	}

	
	/*================================================================================================*/
	public interface ISphereElement<T> : ISphereElement where T : Element, new() {
		
		T Data { get; }
		
	}

}
